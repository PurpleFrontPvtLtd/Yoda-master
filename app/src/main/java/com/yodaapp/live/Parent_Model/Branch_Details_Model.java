package com.yodaapp.live.Parent_Model;


public class Branch_Details_Model {

    String branch_email;
    String branch_phone;
    String branch_address;
    String branch_type;
    String branch_facebook;
    String branch_website;
    String branch_timing;


    public String getBranch_address() {
        return branch_address;
    }

    public void setBranch_address(String branch_address) {
        this.branch_address = branch_address;
    }

    public String getBranch_email() {
        return branch_email;
    }

    public void setBranch_email(String branch_email) {
        this.branch_email = branch_email;
    }

    public String getBranch_facebook() {
        return branch_facebook;
    }

    public void setBranch_facebook(String branch_facebook) {
        this.branch_facebook = branch_facebook;
    }

    public String getBranch_phone() {
        return branch_phone;
    }

    public void setBranch_phone(String branch_phone) {
        this.branch_phone = branch_phone;
    }

    public String getBranch_timing() {
        return branch_timing;
    }

    public void setBranch_timing(String branch_timing) {
        this.branch_timing = branch_timing;
    }

    public String getBranch_type() {
        return branch_type;
    }

    public void setBranch_type(String branch_type) {
        this.branch_type = branch_type;
    }

    public String getBranch_website() {
        return branch_website;
    }

    public void setBranch_website(String branch_website) {
        this.branch_website = branch_website;
    }
}
